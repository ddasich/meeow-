def f(x):
    return x**2 / (10 + x**3)

a = -2
b = 5
n = 10000
h = (b - a) / n
y = []
x = a
while x <= b:  #изменение условие на "<=" для активации правой границы
    y.append(f(x))
    x += h

s = 0
for i in range(len(y) - 1):  #пары соседних точек
    s += h * (y[i] + y[i + 1]) / 2  #формула метода трапеций

print(f"{s:.2f}")
